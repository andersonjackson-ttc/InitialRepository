function theImage() {
    var spotButton = document.getElementById("spot");
    var spotImage = document.createElement("img");
    spotImage.src = "https://static.wikia.nocookie.net/vsbattles/images/6/63/Spot.png/revision/latest/scale-to-width-down/280?cb=20230924184603";
    spotImage.alt = "Spot Image";
    spotButton.appendChild(spotImage);
  }